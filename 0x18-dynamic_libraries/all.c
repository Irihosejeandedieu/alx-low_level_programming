#include <stdio.h>

/**
 * add - add two integers
 * @a :the first value
 * @b:the second integer
 * Return:addition
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two integers
 * @a :the first value
 * @b:the second integer
 * Return:subtraction
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - mod of two integers
 * @a :the first value
 * @b:the second integer
 * Return:final answer
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a :the first value
 * @b:the second integer
 * Return:multplication results
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a :the first value
 * @b:the second integer
 * Return:division results
 */

int div(int a, int b)
{
	return (a / b);
}
